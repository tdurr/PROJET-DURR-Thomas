<?php



/**
 * Client
 */
class Client
{
    /**
     * @var int
     */
    private $id;

    /**
     * @var string|null
     */
    private $nom;

    /**
     * @var string|null
     */
    private $prenom;

    /**
     * @var string|null
     */
    private $adresse;

    /**
     * @var string|null
     */
    private $cp;

    /**
     * @var string|null
     */
    private $ville;

    /**
     * @var string|null
     */
    private $tel;

    /**
     * @var string|null
     */
    private $email;

    /**
     * @var string|null
     */
    private $civilite;

    /**
     * @var string|null
     */
    private $login;

    /**
     * @var string|null
     */
    private $password;


